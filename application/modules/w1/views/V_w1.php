<h6 class="text-dark">
	   Gunung Bromo adalah salah satu gunung berapi aktif yang ada di Indonesia. tepatnya di Jawa Timur dan meliputi 4 kabupaten yaitu Kabupaten Probolinggo, Pasuruan, Lumajang dan Kabupaten Malang. Sebagai gunung berapi yang masih aktif, Bromo jadi tujuan wisata terkenal di Jawa Timur dan hampir tidak pernah sepi setiap harinya.
	   Statusnya yang masih aktif membuat Gunung Bromo jadi lebih menarik di mata wisatawan. Ketinggian Gunung Bromo yaitu 2.392 meter diatas permukaan laut dan memiliki bentuk tubuh bertatutan dia antara lembah dan ngarai dengan dikelilingi kaldera atau lautan pasir luas kurang lebih sekitar 5.300 hektar.
	   Selain itu, terdapat objek wisata lain yang terdapat di Gunung Bromo antara lain Desa Ngadas, Bukit Teletubbis, Bukit Cinta, Bukit Kingkong Bromo, Danau Ranu Pani, Danau Ranu Kumbolo, dan masih banyak lagi.
